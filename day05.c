void bubble_sort(int *seat_ids, int num_entries) 
{
	for (size_t i = 0; i <num_entries; i++) {
		for (size_t j = i+1; j <num_entries; j++) {
			if(*(seat_ids + i) > *(seat_ids + j)) {
				*(seat_ids + i)  = *(seat_ids + i) + *(seat_ids + j) ; 
				*(seat_ids + j)  = *(seat_ids+ i) - *(seat_ids + j) ; 
				*(seat_ids + i)  = *(seat_ids+ i) - *(seat_ids + j) ; 
			}
		}
	}

}

int b_search(int *seat_ids, int num_entries, int value) 
{
	int low = 0; 
	int high = num_entries - 1;
	int mid = (low+high)/2;
	int found = 0;
    while (low <= high) {
		if (seat_ids[mid]  == value) {
			found = 1;
			break;
		}
		else if( seat_ids[mid] > value) {
			high = mid - 1;
		}
		else {
			low = mid +1;
		}
		mid = (low + high)/2;
	}
	return found;
}

