int find_row_column_seat_id(char* data) 
{
     int low_row = 0; 
	 int high_row = 127;
	 int mid_row = (low_row+high_row)/2;
	 
	 int low_column = 0; 
	 int high_column = 7;
	 int mid_column = (low_column+high_column)/2;
	 
	for (size_t i = 0; i< 10; i++) {
		if (i< 7) {
			if( data[i] == 'F') {
				high_row = mid_row;
			}
			else {
				low_row = mid_row + 1;
			}
			mid_row = (low_row+high_row)/2;
		}
		else {
		    if( data[i] == 'L') {
				high_column = mid_column;
			}
			else {
				low_column = mid_column + 1;
			}
			mid_column = (low_column+high_column)/2;
		}
	} 
	int seat_id = mid_row*8 + mid_column;
    //printf("row %u column %u and seat id %u \n", mid_row, mid_column, seat_id);
	return seat_id;
	
}

